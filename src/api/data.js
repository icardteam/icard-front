import axios from '@/libs/api.request'

//调取用户列表接口
export const getUserTableData = () => {
  return axios.request({  // 这里返回的是一个Promise，request方法传入一个配置对象，配置项可参考axios
    url: '/thinkphp5/public/get_users_table_data',
    method: 'get'
  })
}



